export function soma(num1, num2) {
    return num1 + num2
}

export function subtracao(num1, num2) {
    return num1 - num2
}

export function multiplicacao(num1, num2) {
    return num1 * num2
}

export function divisao(num1, num2) {
    return num1 / num2
}

export function potencia(num1, num2) {
    return num1 ** num2
}

export function restoDaDivisao(num1, num2) {
    return num1 % num2
}

export function raizQuadrada(num1, num2) {
    return Math.sqrt(num1)
}